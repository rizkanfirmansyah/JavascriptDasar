// Operator Comparison

let nilai = 70;

let hasil1 = nilai === 70;
let hasil2 = nilai < 70;
let hasil3 = nilai <= 70;
let hasil4 = nilai > 70;
let hasil5 = nilai >= 70;
let hasil6 = nilai !== 70;

console.log(hasil1); // ===
console.log(hasil2); // <
console.log(hasil3); // <=
console.log(hasil4); // >
console.log(hasil5); // >= 
console.log(hasil6); // !==

console.log(hasil1 ? "Hasil ini Benar" : "Hasil Tidak Benar");
console.log(hasil2 ? "Hasil ini Benar" : "Hasil Tidak Benar");
console.log(hasil3 ? "Hasil ini Benar" : "Hasil Tidak Benar");