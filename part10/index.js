// Function Javascript

// 1. Function Declaration/Deklarasi

function sayHello() {
    console.log("Hello World!");
}

sayHello();

// 2. Function Expression/Ekspresi

let tambah = function tambah(a, b) { return a + b }

console.log(tambah(1, 3))

// 3. Arrow Function

const kali = (a, b) => {
    console.log(a * b);
}

kali(3, 4);

// 4. Function Built in

alert("hallo");